    local function cat001()
        local _success, _result = pcall(function()
    local co = coroutine.create(function()
        return pcall(function()
            coroutine.yield(123)
            return 456
        end)
    end)
    local _, r1 = coroutine.resume(co)
    local _, r2, r3 = coroutine.resume(co)
    local ok = (r1 == 123 and r2 == true and r3 == 456)
            return ok
        end)
        local ok = _success and _result == true
        print(ok and "c001_ok" or "c001_dtc")
    end
    cat001()
end

